/**
 * NumberOutOfModeBoundsException Class - Used to indicate that a number is out of bounds in the current mode
 *
 * @author Drew Hans
 */
public class NumberOutOfModeBoundsException extends Exception {

    /**
     * NumberOutOfModeBoundsException Constructor
     *
     * @param message
     */
    public NumberOutOfModeBoundsException(String message) {
        super(message);
    }//end NumberOutOfModeBoundsException Constructor

}//end NumberOutOfModeBoundsException class
